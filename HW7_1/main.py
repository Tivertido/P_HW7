# Задача 34:  Винни-Пух попросил Вас посмотреть, есть ли в его стихах ритм. 
# Поскольку разобраться в его кричалках не настолько просто, насколько легко он их придумывает, Вам стоит написать программу. 
# Винни-Пух считает, что ритм есть, если число слогов (т.е. число гласных букв) в каждой фразе стихотворения одинаковое. 
# Фраза может состоять из одного слова, если во фразе несколько слов, то они разделяются дефисами. Фразы отделяются друг от друга пробелами. 
# Стихотворение  Винни-Пух вбивает в программу с клавиатуры. В ответе напишите “Парам пам-пам”, 
# если с ритмом все в порядке и “Пам парам”, если с ритмом все не в порядке
# *Пример:*
# **Ввод:** пара-ра-рам рам-пам-папам па-ра-па-да    
# **Вывод:** Парам пам-пам  


def count_syllables(phrase):
    vowels = "аеёиоуыэюя"
    phrase = phrase.lower()  # Приводим фразу к нижнему регистру
    count = 0
    for letter in phrase:
        if letter in vowels:
            count += 1
    return count

# Ввод стихотворения Винни-Пуха
poem = input("Введите стихотворение Винни-Пуха: ")

# Разделяем стихотворение на фразы по пробелам
phrases = poem.split()

# Проверяем, есть ли ритм в стихотворении
rhyme = True
syllables_count = None

for phrase in phrases:
    # Подсчитываем число слогов в текущей фразе
    current_syllables_count = count_syllables(phrase)
    
    # Если это первая фраза, сохраняем число слогов
    if syllables_count is None:
        syllables_count = current_syllables_count
    # Если число слогов в текущей фразе отличается от предыдущей, нет ритма
    elif current_syllables_count != syllables_count:
        rhyme = False
        break

# Выводим результат
if rhyme:
    print("Парам пам-пам")
else:
    print("Пам парам")